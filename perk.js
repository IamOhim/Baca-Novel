// Pagination settings
let loadedText = '';
let index = 0;
const wordsPerLoad = 1000; // Number of words per load

// Function to load more text
function loadMore() {
    const words = novelText.split(' ');
    const nextChunk = words.slice(index, index + wordsPerLoad).join(' ');

    loadedText += nextChunk + ' ';
    document.getElementById('novel-text').textContent = loadedText;

    index += wordsPerLoad;

    // Hide button if all text is loaded
    if (index >= words.length) {
        document.getElementById('load-more-btn').style.display = 'none';
    }
}

// Comment Functionality
function addComment(event) {
    event.preventDefault();

    const name = document.getElementById('name').value;
    const comment = document.getElementById('comment').value;
    const commentList = document.getElementById('comment-list');

    // Create new comment element
    const newComment = document.createElement('li');
    newComment.innerHTML = `<strong>${name}:</strong> <p>${comment}</p>`;
    
    commentList.appendChild(newComment);

    // Clear form fields
    document.getElementById('comment-form').reset();
}
